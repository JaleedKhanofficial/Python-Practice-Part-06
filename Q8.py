# Write a program to create a tuple of numbers and find the maximum and minimum values using the max() and min() functions.

print("** Find the Maximum and Minimum Values in a Tuple **")

# Define a tuple of numbers
my_tuple = (15, 3, 20, 7, 10)

# Find the maximum and minimum values
maximum = max(my_tuple)
minimum = min(my_tuple)

# Print the results
print("Maximum value in the tuple:", maximum)
print("Minimum value in the tuple:", minimum)


