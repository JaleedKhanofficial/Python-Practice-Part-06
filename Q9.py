# Write a program to count and print the number of times a specific element appears in a tuple.

print("** Count Occurrences of an Element in a Tuple **")

# Define a tuple
my_tuple = (1, 2, 3, 4, 3, 5, 3, 6)

# Input the element to count
element = int(input("Enter the element to count: "))

# Count the occurrences of the element
count = my_tuple.count(element)

# Print the result
print(f"The element {element} appears {count} times in the tuple.")

