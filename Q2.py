# Write a program to access and print the first, middle, and last elements of a tup

print("** Access Elements in a Tuple **")

# Define a tuple
my_tuple = (5, 10, 15, 20, 25)

# Access and print specific elements
print("First element:", my_tuple[0])          # First element
print("Middle element:", my_tuple[len(my_tuple) // 2])  # Middle element
print("Last element:", my_tuple[-1])          # Last element


